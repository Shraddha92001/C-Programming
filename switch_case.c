#include<stdio.h>

int main ()
{
    int a = 10;
    switch (a)
    {
        case 20:
        printf("the a is 10");
        break;

        case 18:
        printf("the a is 18");
        break;

        case 10:
        printf("the a is 10");
        break;

        case 19:
        printf("the a is 19");
        break;
    
    default:
    printf("a is not 10");
    }
}